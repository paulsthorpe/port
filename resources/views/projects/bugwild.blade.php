    $(document).ready(function() {
        lightBox();

        function lightBox() {
            $('img').each(function(index) {
                $(this).on('click', function() {
                    var src = $(this).attr('src');

                    var theTop = $(window).scrollTop();
                    $('.lightbox-image').attr('src', src).css({
                        'display': 'block',
                        'top': theTop
                    });
                    $('.backdrop').css({
                        'display': 'block',
                        'top': theTop
                    });
                    $('.close').css({
                        'display': 'block',
                        'top': (theTop + 30)
                    });

                    $(window).scroll(function(theTop) {
                        var theTop = $(window).scrollTop();
                        $('.backdrop').css({
                            'top': theTop
                        });
                        $('.lightbox-image').css({
                            'top': theTop
                        });
                        $('.close').css({
                            'display': 'block',
                            'top': (theTop + 30)
                        });
                    });

                    $('.close').click(function() {
                        $('.lightbox-image').css({
                            'display': 'none'
                        });
                        $('.backdrop').css({
                            'display': 'none'
                        });
                        $('.close').css({
                            'display': 'none'
                        });
                    });
                });
            });
        }



    });
</script>
